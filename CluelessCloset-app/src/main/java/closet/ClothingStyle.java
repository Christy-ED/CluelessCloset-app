package closet;

public class ClothingStyle {


    int clothingStyle;
    String casual;

    String formal;

    public void clothingStyle(){

    }
    public String getFormal() {
        return formal;
    }

    public void setFormal(String formal) {
        this.formal = formal;
    }



    public String getCasual() {
        return casual;
    }

    public void setCasual(String casual) {
        this.casual = casual;
    }



    public int getClothingStyle() {
        return clothingStyle;
    }

    public void setClothingStyle(int clothingStyle) {
        this.clothingStyle = clothingStyle;
    }
}
